names = "John, Jennie, Jim, Jack, Joe"

print("names[4] :", names[4])
print("names[-4] :", names[-3])

print(f"names[:6] : {names[:5]}")
print(f"names[:-2] : {names[:-2]}")
print(f"names[-7:-2] : {names[-7:-2]}")

print(f"names*3 : {names*3}")

print(f"names+names :{names+names}")

print("'John' in names :", "John" in names)
print("'George' in names :", "George" not in names)

